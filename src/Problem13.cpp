void AddStringNumbers(const char sn1[101], char sn2[101])
{
    char carry = 0;

    for(int i = 99; i > 0; --i)
    {
        char d1 = sn1[i] - '0';
        char d2 = sn2[i] - '0';
        char sum = d1 + d2 + carry;
        carry = sum / 10;
        char dout = sum % 10;
        sn2[i] = '0' + dout;
    }
}

